import SwiftUI

extension Color {
    public static var darkRed: Color {
        return Color(red: 102.0 / 255.0, green: 0, blue: 0)
    }

    public static var darkYellow: Color {
        return Color(red: 102.0 / 255.0, green: 102.0 / 255.0, blue: 0)
    }
    
    public static var darkGreen: Color {
        return Color(red: 0, green: 102.0 / 255.0, blue: 0)
    }
}
